/**
 * 写作技能包（Skill）系统预置种子。
 *
 * 设计取向（依据用户反馈修正）：
 * - **systemAddon = 写作方法论指令**（要求模型「怎么写」的规则约束），按题材扩充；这不是范文，
 *   不会增加「AI 腔」，反而约束生成方向。
 * - **fewShots = 范文 few-shot**，必须是**真人写的优秀片段**才有降 AI 腔的价值；用 AI 自造范文
 *   反而强化 AI 腔，因此系统预置包**不内置范文**（fewShots 留空），范文由用户按题材自行粘贴
 *   （在「设置 → 写作技能」里编辑技能包添加，或新建自己的技能包）。
 * - 题材匹配：生成时按小说 genre 自动启用「通用 + 同题材」系统包（见 resolveSkillIdsForNovel）。
 *
 * 存储：实体 WritingSkill 的 fewShots / checklist / appliesTo 以 JSON 字符串存储，
 * seed 同步服务落库时 JSON.stringify，读取时由 API / 注入层 JSON.parse。
 */

